#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	n;

	n = nb;
	if (n == -2147483648)
		write (1, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		write (1, "-", 1);
		nb = nb * (-1);
	}
	if (nb <= 9 && n != -2147483648)
	{
		ft_putchar(nb + '0');
	}
	if (nb >= 10)
	{
		ft_putnbr (nb / 10);
		ft_putnbr (nb % 10);
	}
}
